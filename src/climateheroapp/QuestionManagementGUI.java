    private void delQBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delQBtnActionPerformed
        // TODO add your handling code here:
        if (tFRb.isSelected()) {
            int i = 0;
            int j = 0;
            for (TrueOrFalse t : tList) {
                i++;
                if (t.getId() == Integer.parseInt(deleteTf.getText())) {
                    tList.remove(t);
                    JOptionPane.showMessageDialog(null, "The question with id: " + t.getId() + " has been deleted.");
                }
                else{
                    j++;
                }
            }
            if(i==j){
                JOptionPane.showMessageDialog(null, "This ID does not exist!");
            }
        } else if (mCRb.isSelected()) {
            int i = 0;
            int j = 0;
            for (MultipleChoice m : mList) {
                i++;
                if (m.getId() == Integer.parseInt(deleteTf.getText())) {
                    mList.remove(m);
                    JOptionPane.showMessageDialog(null, "The question with id: " + m.getId() + " has been deleted.");
                }
                else{
                    j++;
                }
            }
            if(i==j){
                JOptionPane.showMessageDialog(null, "This ID does not exist!");
            }
        }
    }//GEN-LAST:event_delQBtnActionPerformed

