    private void addQBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addQBtnActionPerformed
        // TODO add your handling code here:
        if (tFRb.isSelected()) {
            question = questionTf.getText();
            correction = correctionTf.getText();
            answ = answerTf.getText();
            level = Integer.parseInt(levelTf.getText());
            option1 = option1Tf.getText();
            option2 = option2Tf.getText();
            
            //This code generates different IDs based on the existing ones, replacing missing numbers so that they can be selected as potential questions
            int count = 1;
            for (TrueOrFalse t : tList) {
                if (count == t.getId()) {
                    count++;
                }
            }
            id = count;

            TrueOrFalse t = new TrueOrFalse();
            t.setQuestion(question);
            t.setCorrection(correction);
            t.setAnsw(answ);
            t.setLevel(level);
            t.setOption1(option1);
            t.setOption2(option2);
            t.setId(id);

            tList.add(t);
            JOptionPane.showMessageDialog(null, "The question with id: " + t.getId() + " has been created.");

        }
        if (mCRb.isSelected()) {
            question = questionTf.getText();
            correction = correctionTf.getText();
            answ = answerTf.getText();
            level = Integer.parseInt(levelTf.getText());
            option1 = option1Tf.getText();
            option2 = option2Tf.getText();
            option3 = option3Tf.getText();
            option4 = option4Tf.getText();

            
          
            int count = 1;
            for (MultipleChoice m : mList) {
                if (count == m.getId()) {
                    count++;
                }
            }
            id = count;

             

            MultipleChoice m = new MultipleChoice();
            m.setQuestion(question);
            m.setCorrection(correction);
            m.setAnsw(answ);
            m.setLevel(level);
            m.setOption1(option1);
            m.setOption2(option2);
            m.setOption3(option3);
            m.setOption4(option4);
            m.setId(id);

            mList.add(m);
            JOptionPane.showMessageDialog(null, "The question with id: " + m.getId() + " has been created.");

        }
    }//GEN-LAST:event_addQBtnActionPerformed

    private void showQBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_showQBtnActionPerformed
        // This code reads the questions which have been saved to the file
        if (mCRb.isSelected()) {
            File inFile;
            FileInputStream fStream;
            ObjectInputStream oStream;

            try {
                inFile = new File("outputM.data");
                fStream = new FileInputStream(inFile);
                oStream = new ObjectInputStream(fStream);

                mList = (ArrayList<MultipleChoice>) oStream.readObject();

                for (MultipleChoice m : mList) {
                    JOptionPane.showMessageDialog(null, "Question: " + m.getQuestion() + ", Answer: " + m.getAnsw() + ", Correction: " + m.getCorrection() + ", Level: " + m.getLevel() + ", Id: " + m.getId() + ", Option 1: " + m.getOption1() + ", Option 2: " + m.getOption2() + ", Option 3: " + m.getOption3() + ", Option 4: " + m.getOption4() + ".");
                }

                oStream.close();
            } catch (IOException e) {
                System.out.println(e);
            } catch (ClassNotFoundException ex) {
                System.out.println(ex);
            }
        } else if (tFRb.isSelected()) {
            File inFile;
            FileInputStream fStream;
            ObjectInputStream oStream;

            try {
                inFile = new File("outputT.data");
                fStream = new FileInputStream(inFile);
                oStream = new ObjectInputStream(fStream);

                tList = (ArrayList<TrueOrFalse>) oStream.readObject();

                for (TrueOrFalse t : tList) {
                    JOptionPane.showMessageDialog(null, "Question: " + t.getQuestion() + ", Answer: " + t.getAnsw() + ", Correction: " + t.getCorrection() + ", Level: " + t.getLevel() + ", Id: " + t.getId() + ", Option 1: " + t.getOption1() + ", Option 2: " + t.getOption2() + ".");
                }
                oStream.close();
            } catch (IOException e) {
                System.out.println(e);
            } catch (ClassNotFoundException ex) {
                System.out.println(ex);
            }
        }
    }//GEN-LAST:event_showQBtnActionPerformed

